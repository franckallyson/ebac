    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int result = tblDados.getSelectedRow();
        
        if(result >= 0){
            int confirmaOpcao = JOptionPane.showConfirmDialog(this, "Deseja atualizar este cliente?", "ATENÇÃO", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(confirmaOpcao == JOptionPane.YES_OPTION){
                
                Cliente novoCliente = new Cliente(txtNome.getText(), txtCpf.getText(), txtTelefone.getText(), txtEndereco.getText(), txtNumero.getText(), txtCidade.getText(), txtEstado.getText());
                this.clienteDAO.alterar(novoCliente);
                
                modelo.setValueAt(novoCliente.getNome(), result, 0);
                modelo.setValueAt(novoCliente.getTel().toString(), result, 2);
                modelo.setValueAt(novoCliente.getCidade(), result, 3);
                modelo.setValueAt(novoCliente.getEstado(), result, 4);
                
                
                JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum cliente selecionado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

